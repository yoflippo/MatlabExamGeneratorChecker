%% Opdracht 6
% Maak een functie genaamd 'opdracht_6' met een inputvariabele
% genaamd 'balSnelheid'.
%
% Zorg dat deze functie respectievelijk het gemiddelde en de mediaan van de
% input terug geeft.

function [gemiddelde, mediaan]= opdracht_6(balSnelheid)

gemiddelde = mean(balSnelheid);
mediaan = median(balSnelheid);

end
