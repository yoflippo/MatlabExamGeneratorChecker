% Gebruik een while-lus om een gegeven getal (in de variabele 'getal') in
% elke herhaling te delen door 2 en er 1 bij op te tellen. Sla het
% resultaat van die bewerking op in diezelfde variabele (anders krijg je een
% eeuwige lus en dat is fout). Herhaal m.b.v. een while-lus zolang het 
% getal groter is dan 


getal = 53;
while getal > 5
    getal = getal / 2;
    getal = getal + 1;
end