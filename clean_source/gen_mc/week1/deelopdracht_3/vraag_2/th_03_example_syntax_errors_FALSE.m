Gegeven is dat in het onderstaande voorbeeld  
het code commentaar (aangegeven met het '%' teken)
aangeeft wat er moet gebeuren.

De code hieronder geeft een voorbeeld van een syntaxfout:

======= Code =======
% Optellen van een variabele
a = 23;
c = 22 %%2;
b = 11;
som = a * c;
