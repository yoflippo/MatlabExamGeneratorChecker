In het onderstaande stuk code wordt een stuk tekst uit de 
variabele tekst geselecteerd. Deze geselecteerde tekst wordt
in de variabele selectie gestopt en bevat alle medeklinkers 
(alle letters behalve a,e,i,o,u).


======= Code =======

tekst = 'hieperdepiephoera!!!';
selectie = tekst([1 4 6 7 9 12 13 16]);

