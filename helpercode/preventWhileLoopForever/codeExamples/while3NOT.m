%% Opdracht 2
% Druk m.b.v. een while-lus de getallen 10 tot en met 98 af in het Command
% Window. Gebruik hiertoe de variabele genaamd 'teller'.
% Als je een eeuwige lus maakt (een lus die niet stopt) krijg je nul punten
% voor deze opdracht.

teller = 10;
while teller < 98
    teller = teller + 1;
end